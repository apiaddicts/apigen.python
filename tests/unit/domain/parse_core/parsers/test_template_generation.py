class TestMapperUnloadedCheck:
    """Verify mapper uses persistence column names in inspect(entity).unloaded checks.

    Bug: mapper.jinja was using attr.name (camelCase e.g. 'firstName') for the
    unloaded check instead of attr.persistence.column (snake_case e.g. 'first_name').
    Since inspect(entity).unloaded returns SQLAlchemy attribute names (snake_case),
    the check using camelCase never matches, causing fields to incorrectly return None.
    """

    def test_parser_provides_persistence_column(self):
        """Parser must expose persistence.column alongside attr.name for each attribute."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=PETSTORE_SPEC)
        parser.get_project()
        parser.get_entities()

        owner = parser.entities.get("Owner")
        assert owner is not None

        attr_map = {}
        for attr in owner.attributes:
            data = attr.model_dump(by_alias=True)
            persistence = data.get("relational-persistence", {})
            attr_map[attr.name] = persistence.get("column") if persistence else None

        # camelCase model name must map to snake_case DB column
        assert attr_map["firstName"] == "first_name", \
            "Parser should map firstName -> first_name column"
        assert attr_map["lastName"] == "last_name", \
            "Parser should map lastName -> last_name column"
        # Same name stays same
        assert attr_map["email"] == "email", \
            "Parser should keep email -> email column"

    def test_address_persistence_column(self):
        """zipCode should map to zip_code column."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=PETSTORE_SPEC)
        parser.get_project()
        parser.get_entities()

        address = parser.entities.get("Address")
        assert address is not None

        for attr in address.attributes:
            if attr.name == "zipCode":
                data = attr.model_dump(by_alias=True)
                col = data.get("relational-persistence", {}).get("column")
                assert col == "zip_code", \
                    "zipCode should map to zip_code column"
                return

        pytest.fail("zipCode attribute not found in Address entity")

    def test_all_attributes_have_persistence(self):
        """Every attribute must have relational-persistence with a column."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=PETSTORE_SPEC)
        parser.get_project()
        parser.get_entities()

        for entity_name, entity in parser.entities.items():
            for attr in entity.attributes:
                data = attr.model_dump(by_alias=True)
                persistence = data.get("relational-persistence")
                assert persistence is not None, \
                    f"{entity_name}.{attr.name} missing relational-persistence"
                assert persistence.get("column") is not None, \
                    f"{entity_name}.{attr.name} missing column in persistence"


