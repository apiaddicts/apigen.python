class TestSmartMappingDetection:
    """Verify Smart Mapping correctly detects array-to-string type mismatches."""

    def test_smart_mapping_detected(self):
        """When schema says array but entity says String, has_json_conversion should be True."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=SMART_MAPPING_SPEC)
        parser.get_project()
        parser.get_entities()

        pet = parser.entities.get("Pet")
        assert pet is not None

        for attr in pet.attributes:
            if attr.name == "photoUrls":
                assert attr.has_json_conversion is True, \
                    "photoUrls should have has_json_conversion=True (array in schema, String in entity)"
                return

        pytest.fail("photoUrls attribute not found in Pet entity")

    def test_normal_string_no_json_conversion(self):
        """Normal string fields should not have json_conversion."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=SMART_MAPPING_SPEC)
        parser.get_project()
        parser.get_entities()

        pet = parser.entities.get("Pet")
        for attr in pet.attributes:
            if attr.name == "name":
                assert attr.has_json_conversion is False, \
                    "name is a simple String, should not have json_conversion"
                return

    def test_smart_mapping_persistence_column(self):
        """Smart Mapping field should still have correct persistence column."""
        parser = OpenAPIParser()
        parser.load_definition(spec_str=SMART_MAPPING_SPEC)
        parser.get_project()
        parser.get_entities()

        pet = parser.entities.get("Pet")
        for attr in pet.attributes:
            if attr.name == "photoUrls":
                data = attr.model_dump(by_alias=True)
                col = data.get("relational-persistence", {}).get("column")
                assert col == "photo_urls", \
                    "photoUrls should map to photo_urls column"
                return
